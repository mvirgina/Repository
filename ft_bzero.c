#include "libft.h"
#include <stddef.h>

void	bzero(void *s, size_t n)
{
	size_t	i;

	if (!s)
		return;
	i = 0;
	while (i < n)
	{
		*(char*)(s + i) = 0;
		i++;
	}
}
