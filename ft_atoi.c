#include "libft.h"

int	ft_atoi(const char *str)
{
	int			i;
	long int	is_neg;
	long int	res;

	i = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v' ||
			str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	is_neg = (str[i] == '-') ? -1 : 1;
	if (is_neg == -1 || str[i] == '+')
		i++;
	res = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = (res * 10) + (str[i] - '0');
		if ((res * is_neg) > 2147483647)
			return (-1);
		if ((res * is_neg) < -2147483648)
			return (0);
		i++;
	}
	return ((int)(res * is_neg));
}
